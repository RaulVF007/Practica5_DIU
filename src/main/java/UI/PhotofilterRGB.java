    private void RedButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RedButtonActionPerformed
        redFilter = RedButton.isSelected();
        blueFilter = BlueButton.isSelected();
        greenFilter = GreenButton.isSelected();
        
        if(greenFilter || blueFilter){
            TodosButton.setSelected(false);
            this.lienzo1.setFilters(RedButton.isSelected(),GreenButton.isSelected(),BlueButton.isSelected());
        }else{
            RedButton.setSelected(true);
        }
        
        if(redFilter && blueFilter && greenFilter){
            TodosButton.setSelected(true);
        }
    }//GEN-LAST:event_RedButtonActionPerformed

